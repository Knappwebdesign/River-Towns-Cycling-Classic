            <h2>Log Pull</h2>
            <p>As with the Tug of War, the log pull is a test of power and balance. However, the log pull is for those that like their adversary inadimate and covered with bark. There will be bundles of logs and chain at the starting line. Each participant will be required to attach the chain to their seat post and pull the pile of logs to the finish line. Why the seat post? Well, because our medical team told us to stop attaching the chain to rider’s necks, and so the seat post was the next skinny thing we could think of that would cause maximum leverage against the rider.</p>

            <p>If the rider dabs during the pull, they will be registered at the highest number of logs they pulled successfully. In other words, the last successful pull. In the event of a tie, a winner will be decided in a duel situation, in which the two remaining riders will take 5 paces away from each other, and then throw spoke tools at each other until one or the other rider is incapacitated.</p>

