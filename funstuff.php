            <p>Who doesn’t love a kids event? Those cute little boys and girls sticking their tongues out just a little bit as they concentrate real hard on navigating the course. It’s adorable! And let’s be honest, it’s the future of our sport. Kids just aren’t riding these days. So maybe if we can convince them to have some fun and earn valuable prizes, trophies and medals in the process, we can help them get interested in riding and maybe a little less “Halo” or “Grand Theft Auto”.</p>

            <p>These kinds of events NEED to be flexible. Parents: come be a part of it, with that in mind. Organizers have been doing this for 10+ years. What we’ve learned is kids are unpredictable. And we LOVE that unpredictability! So we will adjust on the fly and respond to the curves the kids throw at us on the event day. THE GOAL IS FOR EVERYONE TO HAVE FUN! If that is not what you want for your kid, Organizers would advise you to stay away from this event. The more formal, timed, sanctioned, and serious events going on all weekend long all have beginner categories, and that might be a better fit for you and junior Lance Armstrong.</p>

